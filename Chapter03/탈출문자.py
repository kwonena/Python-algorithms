# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")

# 저는 "이나"입니다.
print("저는 '이나'입니다.")
print('저는 "이나"입니다.')
print("저는 \"이나\"입니다.")
print("저는 \'이나\'입니다.")

# \\ : 문장 내에서 \
print("C:\\Users\\이나\\Documents\\ENA\\Python-basic-study")

# \r : 커서를 맨 앞으로 이동
print("BlueApple\rPine") # Blue가 Pine으로 바뀜 

# \t : 탭
print("Red\tApple")